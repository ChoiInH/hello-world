package day04;

public class CoffeeMachine {
	private int coffee;
	private int sugar;
	private int cream;

	// setter----------
	public void setCoffee(int c) {
		coffee = c;
	}// ----------------

	public void setSugar(int s) {
		sugar = s;
	}// ---------------

	public void setCream(int c) {
		cream = c;
	}// ---------------

	// getter--------
	public int getCoffee() {
		return coffee;
	}// --------------

	public int getSugar() {
		return sugar;
	}// --------------

	public int getCream() {
		return cream;
	}// --------------

}
